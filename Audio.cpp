#include "Audio.h"

//=============================================================================
// default constructor
//=============================================================================
Audio::Audio(std::string source)
{
	_source = source;
	buffer.loadFromFile(_source);
	sound.setBuffer(buffer);
}

//=============================================================================
// deconstructor
//=============================================================================
Audio::~Audio()
{
}

//=============================================================================
// playBGM
// play background music of the game
//=============================================================================
void Audio::play()
{
	sound.play();
}

//=============================================================================
// stopBGM
// stop background music of the game
//=============================================================================
void Audio::stop()
{
	sound.stop();
}

//=============================================================================
// setLoop
// stop background music of the game
//=============================================================================
void Audio::setLoop(bool b)
{
	sound.setLoop(b);
}